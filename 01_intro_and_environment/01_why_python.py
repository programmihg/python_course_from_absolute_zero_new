"""!
Защо Python е популярен, неговите характеристики (лаконичен синтаксис, нисък праг на влизане, 
многофункционалност) и области на приложение
!"""
import sys  # Внасяме системния модул 'sys', за да извлечем информация за вашата Python среда


def main():  # Дефинираме главна функция 'main', която ще съдържа основната логика на програмата
    print("--- Добре дошли в курса Python от Абсолютната Нула! ---")  # Извеждаме приветствие на екрана

    # Използваме f-string (форматиран стринг), за да вмъкнем версията на Python директно в текста
    # sys.version съдържа информация за текущата версия на интерпретатора
    print(f"Вие използвате Python версия: {sys.version}")

    print("\nЗащо Python?")  # Извеждаме заглавие, като \n добавя нов празен ред отгоре

    # Създаваме списък (list), който съдържа текстови низове с предимствата на езика
    reasons = [
        "Чист и четим синтаксис",
        "Огромна библиотека от модули",
        "Подходящ за Scripting, Web, AI и Data Science"
    ]

    # Използваме цикъл 'for' заедно с функцията 'enumerate', за да преминем през списъка
    # 'i' ще бъде индексът (започващ от 1), а 'reason' ще бъде самият текст от списъка
    for i, reason in enumerate(reasons, 1):
        print(f"{i}. {reason}")  # Извеждаме номериран ред с предимството

    # Функцията input() спира програмата и чака потребителят да напише нещо в терминала
    # Резултатът се записва в променливата 'name'
    name = input("\nКак се казваш? ")

    # Използваме променливата 'name', за да изведем персонализирано съобщение
    print(f"Приятно ми е да се запознаем, {name}! Проектът ти е настроен правилно.")


# Този блок проверява дали файлът се изпълнява директно (а не е внесен като модул в друг файл)
# Ако е изпълнен директно, се извиква функцията main()
if __name__ == "__main__":
    main()