# Тема: Нива на абстракция - Как Python взаимодейства с хардуера
import platform  # Модул за информация за платформата


def main():
    # Python е език от високо ниво (High-level language)
    # Ние пишем разбираем текст, който интерпретаторът превежда

    print("--- Нива на абстракция в програмирането ---")

    # 1. Ниво на потребителя (Високо ниво - Python)
    concept = "Ние пишем: print('Hello')"
    print(f"1. Високо ниво: {concept}")

    # 2. Ниво на интерпретатора
    # Той превръща нашия код в 'Bytecode'
    print("2. Средно ниво: Python интерпретаторът превежда кода.")

    # 3. Ниво на хардуера (Ниско ниво - Машинен код)
    # Процесорът разбира само 0 и 1
    architecture = platform.machine()  # Извличаме архитектурата на вашия процесор
    print(f"3. Ниско ниво: Вашият процесор ({architecture}) изпълнява инструкциите.")

    print("\nИзвод: Python ни спестява нуждата да познаваме сложния хардуер.")


if __name__ == "__main__":
    main()